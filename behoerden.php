<h1>Ermittlung Zust&auml;ndigkeit</h1>
In Frankfurt gibt es, im Gegensatz zu den meisten anderen deutschen St&auml;dten, mehrere Beh&ouml;rden, die etwas mit Falschparkern zu tun haben k&ouml;nnten. Wer endlose Weiterleitungen vermeiden m&ouml;chte, sollte sich vorher kurz informieren:

<h1 id="schutzpolizei">Schutzpolizei</h1>
<p>Meist nur "Polizei" genannt. Ist wie im Rest von Deutschland unabh&auml;ngig von der Stadt und zust&auml;ndig f&uuml;r alles was mit Kriminalit&auml;t und Gefahrenabwehr zu tun hat. Das Stadtgebiet ist in <a href="https://www.polizei.hessen.de/Dienststellen/Polizeipraesidium-Frankfurt/Ueber-uns/Polizeireviere/">19 Reviere</a> unterteilt. Meldungen k&ouml;nnen entweder per Telefon oder direkt vor Ort aufgegeben werden. Das f&uuml;r eine Adresse zust&auml;ndige Revier kann <a href="https://www.polizei.hessen.de/Dienststellen-Suche/">hier</a> ermittelt werden.</p>
<p>
Die Kontaktdaten variieren je nach Revier, das Schema der Telefonnummer ist immer "069 7551 (Reviernummer mit zwei Ziffern) 00". Bei Gefahr kann auch die zentrale 110 gew&auml;hlt werden: Eine Gefahr besteht regelm&auml;&szlig;ig, wenn ein*e Verkehrsteilnehmer*in von dem vorgesehen Sonderfahrstreifen z.B. auf die Fahrbahn ausweichen muss<a href="#quellen_4"><sup>4</sup></a>.
<br><br>
Arbeitszeiten: 365 Tage im Jahr und rund um die Uhr
<br>
<br>
<a href="https://twitter.com/Polizei_Ffm">Die Landespolizei ist auch auf Twitter vertreten</a>
</p>

<h1 id="staedtische_verkehrspolizei">St&auml;dtische Verkehrspolizei</h1>
<p>Fr&uuml;her "Stadtpolizei-Verkehrssicherheit". Geh&ouml;rt zum Strassenverkehrsamt der Stadt Frankfurt. Die Hilfspolizist*innen sind zust&auml;ndig f&uuml;r Dinge wie Falschparker. Au&szlig;erhalb der Dienstzeiten, bei sonstiger Nichterreichbarkeit oder zu langer Wartezeit sollte die <a href="#schutzpolizei">Schutzpolizei</a> kontakiert werden, diese &uuml;bernimmt im Zweifelsfall die Auftr&auml;ge. <a href="#quellen_1"><sup>1</sup></a></p>
<p>
Kontaktdaten <a href="#quellen_2"><sup>2</sup></a>
<br>
<b>Verkehrssicherheitstelefon: 069 212 36360</b>
<br>
Arbeitszeiten:
<br>
Montag bis Freitag 07:00 - 22:00 Uhr
<br>
Samstag 10:00 - 18:00 Uhr
</p>
<p>
&Uuml;ber den <a href="https://twitter.com/Stadt_FFM">Twitter-Account der Stadt Frankfurt</a> werden auch generelle Anfragen zur st&auml;dtischen Verkehrspolizei beantwortet, zu konkreten Eins&auml;tzen o.&auml;. k&ouml;nnen die Kolleg*innen dort allerdings normalerweise nichts sagen.
</p>

<h1 id="stadtpolizei">Stadtpolizei / Ordnungsamt</h1>
<p>Geh&ouml;rt zum Ordnungsamt der Stadt Frankfurt. Sowohl Ordnungsamt als auch Stadtpolizei haben mit Falschparkern nichts zu tun, auch wenn sie theoretisch die Befugnis haben. <a href="#quellen_3"><sup>3</sup></a></p>

<h1 id="strassenverkehrsamt">Stra&szlig;enverkehrsamt</h1>
Das Stra&szlig;enverkehrsamt hat, unabh&auml;ngig von der unterstellten <a href="#staedtische_verkehrspolizei">St&auml;dtischen Verkehrspolizei</a>, noch weitere Kr&auml;fte zur Verkehrs&uuml;berwachung im Einsatz. Diese Personen sind allerdings keine ausgebildeten Hilfspolizisten und k&ouml;nnen daher keine hoheitlichen Aufgaben (bspw. abschleppen) wahrnehmen. Inwiefern sich das Aufgabengebiet dieser MitarbeiterInnen zum Ordnungsamt unterscheidet ist v&ouml;llig unklar.


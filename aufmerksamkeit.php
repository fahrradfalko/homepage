<h1 id="oeffentlichkeit">&Ouml;ffentlichkeitsarbeit</h1>
<p>
Um Beh&ouml;rden und Politik zum Handeln zu bewegen muss das Thema Falschparken und die dadurch entstehenden Probleme in die &Ouml;ffentlichkeit ger&uuml;ckt werden. Hierzu gibt es mehrere M&ouml;glichkeiten f&uuml;r alle Interessierten:
<ul>
<li>In sozialen Netzwerken auf das Problem aufmerksam machen</li>
<li>Kontaktierung der lokalen Presse mit der Bitte das Thema Falschparken aufzugreifen, im Idealfall mit konkreten Fallbeispielen und Bildern</li>
<li>Leserbriefe zum Thema an die lokale Presse schicken</li>
</ul>
</p>

<h1 id="behoerden">Beh&ouml;rden</h1>
<p>
Selbstverst&auml;ndlich kann und sollte man zus&auml;tzlich auch nicht-&ouml;ffentlich Gespr&auml;che mit den Beh&ouml;rden suchen. Hier bieten sich sowohl das Stra&szlig;enverkehrsamt (-><a href="behoerden.html#staedtische_verkehrspolizei">St&auml;dtische Verkehrspolizei</a>) als auch die Landespolizei an.

Es kann beispielsweise um die h&auml;ufigere Kontrolle an Schwerpunkten gebeten werden. Falls es einmal zu konkreten Problemen mit MitarbeiterInnen kommen sollte k&ouml;nnen bei den einzelnen Beh&ouml;rden formlose Beschwerden sowie Dienstaufsichtsbeschwerden &uuml;ber das konkrete Vorgehen von MitarbeiterInnen eingreicht werden, dies geht an jeder Dienststelle. Hierf&uuml;r sollten Datum, Uhrzeit, Ort, Problem sowie Namen bzw. Dienstnummern der MitarbeiterInnen vorliegen.
</p>

<h1 id="politik">Politik</h1>
<p>
Zentrale Entscheidungen, gerade was die Personalplanung bei st&auml;dtischen Beh&ouml;rden angeht, werden auf politischer Ebene getroffen. Daher ist es wichtig auch auf diesem Kanal das Thema Falschparken ins Bewusststein zu rufen. Dies kann auf folgenden Wegen geschehen:
<ul>
<li>Kontaktieren der <a href="http://www.frankfurt.de/sixcms/detail.php?id=3467">Ortsbeir&auml;te</a> mit der Bitte f&uuml;r mehr Kontrollen zu sorgen bzw. diese bei der Stadt anzufordern. Einfach eine Mail schicken oder pers&ouml;nlich bei Beginn einer Sitzung in der B&uuml;rgerfragestunde etwas sagen</li>
<li>Ansprechen der <a href="http://www.stvv.frankfurt.de/stvvorg/P_STVV.htm">Stadtverordneten</a> mit der Bitte die Themen Kontrollen, Finanzierung und Ma&szlig;nahmen auf die Tagesordnung zu bringen oder Anfragen zu diesen Themen zu stellen</li>
</ul>
</p>

<p>
<b>Egal welchen Weg man geht - jede Hilfe, sei es nur eine E-Mail oder Anruf, z&auml;hlt!</b>
</p>


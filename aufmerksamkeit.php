<h1 id="oeffentlichkeit">&Ouml;ffentlichkeitsarbeit</h1>
<p>
Um Beh&ouml;rden und Politik zum Handeln zu bewegen muss das Thema Falschparken in die &Ouml;ffentlichkeit ger&uuml;ckt werden. Es gibt mehrere M&ouml;glichkeiten:
<ul>
<li>In sozialen Netzwerken auf das Problem aufmerksam machen</li>
<li>Kontaktierung der lokalen Presse mit der Bitte das Thema Falschparken aufzugreifen, im Idealfall mit konkreten Fallbeispielen und Bildern</li>
<li>Leserbriefe zum Thema an die lokale Presse schicken</li>
</ul>
</p>

<h1 id="behoerden">Beh&ouml;rden</h1>
<p>
N&uuml;tzlich sind auch Gespr&auml;che mit den Beh&ouml;rden. Hier bieten sich sowohl das Stra&szlig;enverkehrsamt (-><a href="behoerden.html#staedtische_verkehrspolizei">St&auml;dtische Verkehrspolizei</a>) als auch die Landespolizei an.

Es kann beispielsweise um die h&auml;ufigere Kontrolle an Schwerpunkten gebeten werden. Falls es einmal zu konkreten Problemen mit Mitarbeiter*innen kommen sollte, k&ouml;nnen bei den einzelnen Beh&ouml;rden formlose Beschwerden sowie Dienst- und Fachaufsichtsbeschwerden &uuml;ber das konkrete Vorgehen von Mitarbeiter*innen eingreicht werden, dies geht an jeder Dienststelle. Hierf&uuml;r sollten Datum, Uhrzeit, Ort, Problem sowie Namen bzw. Dienstnummern der Mitarbeiter*innen vorliegen. Wir helfen dir dabei!
</p>

<h1 id="politik">Politik</h1>
<p>
Die Rahmen für Personalplanung etc. werden auf politischer Ebene getroffen. Daher ist es wichtig auch auf diesem Kanal das Thema Falschparken ins Bewusststein zu rufen. Folgende Möglichkeiten gibt es:
<ul>
<li>Kontaktieren der <a href="https://frankfurt.de/service-und-rathaus/stadtpolitik/ortsbeiraete">Ortsbeir&auml;te</a> mit der Bitte f&uuml;r mehr Kontrollen zu sorgen bzw. diese bei der Stadt anzufordern. Einfach eine Mail schicken oder pers&ouml;nlich bei Beginn einer Sitzung in der B&uuml;rgerfragestunde melden</li>
<li>Ansprechen der <a href="http://www.stvv.frankfurt.de/stvvorg/P_STVV.htm">Stadtverordneten</a> mit der Bitte die Themen Kontrollen, Finanzierung und Ma&szlig;nahmen auf die Tagesordnung zu bringen oder Anfragen zu diesen Themen zu stellen</li>
</ul>
</p>
</p>

